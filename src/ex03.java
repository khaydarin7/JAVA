
public class ex03 {

	public static void main(String[] args) {
		//System.out.printf - JAVA언어
		//System클래스의 out객체 내부의 printf메서드
		//자바에서는 함수라고 안하고, 메서드...
		//클래스 안에 들어있는 함수를 메서드...
		//printf 메서드
		//클래스 안에 있는 함수를 메서드라고 한다.
		
		System.out.printf("이름:%s\n","김철수");
		//" " : 쌍따옴표로 둘러쌓이면 문자열...
		//%s - string (문자열)
		//string이라는 단어...는 원래 컴퓨터의 것이 아니고
		//string - 줄,끈,현악기의현
		//컴퓨터에서 문자가 줄처럼 나열된 모양을 문자열
		//string
		//문자가 줄처럼 나열된 모양을 string (문자열)
		//bit도 원래 컴퓨터 단어가 아니고
		//bit도 작은조각...때가쏙 bit
		System.out.printf("나이:%d살\n", 20);
		//\n - 줄바꿈 문자
		//%d - 10진수 정수 서식문자
		//%d - decimal (10진수 정수)
		//서식문자 - 값의 형태를 표현하는 문자...
		System.out.printf("키:%.1fcm\n", 176.57);
		//%f - float 
		//실수를 의미하는 서식문자
		//%f는 원형 그대론데... 
		//%.1f - 소수점 2째자리에서 반올림해서 1째자리까지
		System.out.printf("혈액형:%c형\n",'A');
		//'A' - 문자 A
		//%c - character (문자)
		//문자를 의미하는 서식문자
		
		//printf - C언어
		//printf함수
		
		//C언어와 JAVA의 차이
		//C언어:절차지향언어
		//JAVA언어:객체지향 언어
		//클래스가 있냐? 없냐? 객체지향 언어를 가르는 기준...
		//클래스가 존재한다. 객체지향언어...없으면
		
	}
}
