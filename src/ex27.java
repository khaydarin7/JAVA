
public class ex27 {

	public static void main(String[] args) {
		
		int i=1;//초기식
		int dan=7;
		
		while(i<=9)//i가 9이하인 동안 
		{//조건식
			System.out.printf("%dx%d=%d\n", dan,i,dan*i);
			i++;//증가식
		}
		
		//디버깅
		//debugging
		//debug + ing
		//debug : 버그제거하다.
		//bug : 버그,오류
		//컴퓨터에서 최초의 오류를 일으킨게 벌레다.
		//나방이 컴퓨터에 들어가서 오류가 났다.
		
		//de+bug : 버그를 제거하다.
		//de:박탈,제거의 의미의 접두사
		//destruction:파괴 (제거하다,구조)
		//detox:해독 (제거하다,독)
		
		//디버그는 여기서는 일종의 수동실행과같다.
	}
}
