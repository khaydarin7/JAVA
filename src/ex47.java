
public class ex47 {

	public static void main(String[] args) {	
		//switch~case
		//if~else if~else
		int num=4;
		
		switch(num) {
		case 1:
			System.out.println("One!");
			break;
		case 2:
			System.out.println("Two!");
			break;
		case 3:
			System.out.println("Three");
			break;
		default:
			System.out.println("I don't know");
			break;
		}	
	}
}
