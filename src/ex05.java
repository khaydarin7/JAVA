
public class ex05 {

	public static void main(String[] args) {
		//변수
		//variable (변수)
		//vari + able
		//vari : 다양한
		//able : 할수있는
		//다양하게~~할수있는
		//변수:변할수있는 수
		//값을 저장하는 메모리 공간, 값을 변경가능
		int num1=10;
		float num2=3.14f;
		//float형 변수 num2를 만들고 3.14f로 초기화
		double num3=3.8;
		//double형 변수 num3를 만들고 3.8로 초기화
		//float,double이나 둘다 실수타입이다...
		//double을 선호한다...
		//실수의 표준이 double이기 때문에
		char num4='A';
		//char형 변수 num4를 만들고, 문자 A('A')를 넣겠다.
		String num5="kim";
		//String형 변수 num5를 만들고 문자열("kim")을 넣겠다.
		
		System.out.printf("%d\n",num1);//10
		System.out.printf("%f\n",num2);//3.14
		System.out.printf("%f\n",num3);//3.8
		System.out.printf("%c\n",num4);//'A'
		System.out.printf("%s\n",num5);//"kim"
		//int num=10;
		//int형 변수 num을 선언하고 10으로 초기화
		//정수형 변수 num을 선언하고 10으로 초기화
		//정수형 변수 num을 메모리에 생성하고 10으로 초기화
		//= : 대입 연산자
		// (이거 절대로 같다가 아니다!)
		//오른쪽의 값을 왼쪽의 공간에 넣는다.
		//방향이 오른쪽에서 왼쪽
		//int - integer (정수)
		//%d - 서식문자 중에서 10진수 정수를 의미하는 서식문자

	}
}
