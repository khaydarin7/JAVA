
public class ex11 {

	public static void main(String[] args) {
		int num1=9;
		int num2=2;
		//사칙 연산자 (+ , - , * , / )
		System.out.printf("%d+%d=%d\n", num1,num2,num1+num2);
		System.out.printf("%d-%d=%d\n", num1,num2,num1-num2);
		System.out.printf("%dx%d=%d\n", num1,num2,num1*num2);
		System.out.printf("%d÷%d=%d\n", num1,num2,num1/num2);
	}
}
